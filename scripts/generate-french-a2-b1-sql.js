#!/usr/bin/env node
// scripts/generate-french-a2-b1-sql.js
// Generates supabase/french-a2-b1-content.sql from lib/seed-lessons.json +
// lib/seed-units.json (already built by build-french-a2-seed.js and
// build-french-b1-seed.js from scripts/content/french-{a2,b1}-units.js),
// following exactly the same normalized-schema shape that
// scripts/migrate-french-a1-units.js already writes at runtime via the JS
// Supabase client - this script just emits the equivalent as plain SQL so
// it can be pasted into the Supabase SQL Editor instead of run from Node.
//
// Schema source of truth (inspected, not guessed):
//   supabase/migrations/202607120001_normalized_courses_schema.sql
//     languages(code unique), levels(code unique), courses(language_id,level_id unique),
//     course_lessons(slug unique), lesson_sections (no unique key), exercises (no unique key),
//     exercise_options (no unique key)
//   supabase/migrations/202607170001_course_units.sql
//     course_units(slug unique), course_lessons.unit_id
//   supabase/migrations/202607180001_reading_parts.sql
//     lesson_sections.reading_parts/reading_ordering, exercises.type += 'ordering'
//   supabase/migrations/202607200001_dialogue_skill_and_mission_fields.sql
//     course_lessons.mission/grammar_note/phrases, skill += 'dialogue'
//
// Idempotency strategy:
//   - languages/levels/courses/course_units/course_lessons: INSERT ... ON
//     CONFLICT ... DO UPDATE on their real unique constraints.
//   - lesson_sections/exercises/exercise_options have no natural unique key
//     (same situation the JS migration script already handles), so this
//     script DELETEs existing rows scoped to exactly the French A2/B1
//     lessons being written, then INSERTs fresh ones - re-running this file
//     is safe and never touches any other course/lesson/language.
//
// Usage: node scripts/generate-french-a2-b1-sql.js
const fs = require('fs');
const path = require('path');

const ROOT = path.join(__dirname, '..');
const seedLessons = require(path.join(ROOT, 'lib/seed-lessons.json'));
const seedUnits = require(path.join(ROOT, 'lib/seed-units.json'));
const OUT_PATH = path.join(ROOT, 'supabase/french-a2-b1-content.sql');

const LANGUAGE = 'french';
const LEVELS = [
  { code: 'A2', name: 'A2 - Élémentaire', sortOrder: 2, title: 'Français A2' },
  { code: 'B1', name: 'B1 - Intermédiaire', sortOrder: 3, title: 'Français B1' }
];

// ---------------------------------------------------------------------
// SQL literal helpers
// ---------------------------------------------------------------------
function sqlString(value) {
  if (value === null || value === undefined) return 'NULL';
  return `'${String(value).replace(/'/g, "''")}'`;
}
function sqlJsonb(value) {
  if (value === null || value === undefined) return 'NULL';
  return `${sqlString(JSON.stringify(value))}::jsonb`;
}
function sqlBool(value) {
  return value ? 'true' : 'false';
}
function sqlInt(value, fallback = 0) {
  const n = Number(value);
  return Number.isFinite(n) ? String(n) : String(fallback);
}

function shapeReading(reading) {
  if (!reading) return null;
  if (!reading.parts) return reading;
  return { ...reading, text: reading.parts.join('\n\n') };
}

let out = [];
function line(s = '') {
  out.push(s);
}

line('-- =========================================================');
line('-- supabase/french-a2-b1-content.sql');
line('-- =========================================================');
line('-- Auto-generated by scripts/generate-french-a2-b1-sql.js from');
line('-- scripts/content/french-a2-units.js and scripts/content/french-b1-units.js');
line('-- (via lib/seed-lessons.json / lib/seed-units.json). DO NOT hand-edit -');
line('-- change the content source files and regenerate instead.');
line('--');
line('-- Inserts/updates ONLY French A2 (10 units / 70 activities) and');
line('-- French B1 (10 units / 70 activities). Never touches French A1, any');
line('-- other language, user_lesson_progress, user_exercise_attempts,');
line('-- profiles, auth.users, or any evaluation/attempt table - those are');
line('-- untouched by every statement below (see the scoped DELETEs, which');
line('-- filter strictly by course_lessons.slug LIKE \'french-a2-%\' / \'french-b1-%\').');
line('--');
line('-- Contains no secrets/keys - safe to paste into the Supabase SQL Editor.');
line('-- Run: open this file, copy it ENTIRELY, paste into Supabase SQL Editor, Run.');
line('-- =========================================================');
line();
line('BEGIN;');
line();

// ---------------------------------------------------------------------
// 1. languages
// ---------------------------------------------------------------------
line('-- ---------------------------------------------------------------------');
line('-- 1. languages (upsert french; never touches other languages)');
line('-- ---------------------------------------------------------------------');
line(
  `INSERT INTO public.languages (code, name)\nVALUES ('french', 'Français')\nON CONFLICT (code) DO UPDATE SET name = EXCLUDED.name;`
);
line();

// ---------------------------------------------------------------------
// 2. levels
// ---------------------------------------------------------------------
line('-- ---------------------------------------------------------------------');
line('-- 2. levels (upsert A2/B1; never touches A1/B2/C1/C2 rows)');
line('-- ---------------------------------------------------------------------');
LEVELS.forEach(({ code, name, sortOrder }) => {
  line(
    `INSERT INTO public.levels (code, name, sort_order)\nVALUES (${sqlString(code)}, ${sqlString(name)}, ${sortOrder})\nON CONFLICT (code) DO UPDATE SET name = EXCLUDED.name, sort_order = EXCLUDED.sort_order;`
  );
});
line();

// ---------------------------------------------------------------------
// 3. courses
// ---------------------------------------------------------------------
line('-- ---------------------------------------------------------------------');
line('-- 3. courses (upsert French A2/B1 course rows on their real unique');
line('--    constraint courses(language_id, level_id))');
line('-- ---------------------------------------------------------------------');
LEVELS.forEach(({ code, title }) => {
  const description =
    code === 'A2'
      ? 'Français élémentaire : achats, restaurants et vie quotidienne, organisés en unités thématiques.'
      : 'Français intermédiaire : projets, opinions et expériences, organisés en unités thématiques.';
  line(
    `INSERT INTO public.courses (language_id, level_id, title, description)\nSELECT l.id, lv.id, ${sqlString(title)}, ${sqlString(description)}\nFROM public.languages l, public.levels lv\nWHERE l.code = 'french' AND lv.code = ${sqlString(code)}\nON CONFLICT (language_id, level_id) DO UPDATE SET title = EXCLUDED.title, description = EXCLUDED.description;`
  );
});
line();

// ---------------------------------------------------------------------
// 4. course_units
// ---------------------------------------------------------------------
line('-- ---------------------------------------------------------------------');
line('-- 4. course_units (upsert on slug; French A2/B1 unit slugs only)');
line('-- ---------------------------------------------------------------------');
LEVELS.forEach(({ code }) => {
  const units = seedUnits
    .filter((u) => u.target_language === LANGUAGE && u.level === code)
    .sort((a, b) => (a.order_index || 0) - (b.order_index || 0));
  units.forEach((u) => {
    line(
      `INSERT INTO public.course_units (course_id, slug, title, description, order_index)\nSELECT c.id, ${sqlString(u.slug)}, ${sqlString(u.title)}, ${sqlString(u.description || '')}, ${sqlInt(u.order_index)}\nFROM public.courses c\nJOIN public.languages l ON l.id = c.language_id\nJOIN public.levels lv ON lv.id = c.level_id\nWHERE l.code = 'french' AND lv.code = ${sqlString(code)}\nON CONFLICT (slug) DO UPDATE SET title = EXCLUDED.title, description = EXCLUDED.description, order_index = EXCLUDED.order_index;`
    );
  });
});
line();

// ---------------------------------------------------------------------
// 5. course_lessons
// ---------------------------------------------------------------------
line('-- ---------------------------------------------------------------------');
line('-- 5. course_lessons (upsert on slug; French A2/B1 activity slugs only)');
line('-- ---------------------------------------------------------------------');
const allLessons = [];
LEVELS.forEach(({ code }) => {
  const lessons = seedLessons
    .filter((r) => r.target_language === LANGUAGE && r.level === code)
    .sort((a, b) => (a.order_index || 0) - (b.order_index || 0));
  lessons.forEach((row) => allLessons.push(row));
});

allLessons.forEach((row) => {
  const content = row.content_json || {};
  line(
    `INSERT INTO public.course_lessons\n  (course_id, unit_id, slug, skill, title, description, order_index, xp_reward, access_tier, estimated_minutes, is_published, mission, grammar_note, phrases)\nSELECT c.id, u.id, ${sqlString(row.slug)}, ${sqlString(row.skill)}, ${sqlString(row.title)}, ${sqlString(row.description || content.mission || '')}, ${sqlInt(row.order_index)}, ${sqlInt(content.xp_reward, 20)}, ${sqlString(row.access_tier || (row.is_free === false ? 'premium' : 'free'))}, ${sqlInt(row.estimated_minutes, 10)}, true, ${sqlString(content.mission || null)}, ${sqlString(content.grammar || null)}, ${content.phrases && content.phrases.length ? sqlJsonb(content.phrases) : 'NULL'}\nFROM public.courses c\nJOIN public.languages l ON l.id = c.language_id\nJOIN public.levels lv ON lv.id = c.level_id\nJOIN public.course_units u ON u.course_id = c.id AND u.slug = ${sqlString(row.unit_slug)}\nWHERE l.code = 'french' AND lv.code = ${sqlString(row.level)}\nON CONFLICT (slug) DO UPDATE SET\n  unit_id = EXCLUDED.unit_id, skill = EXCLUDED.skill, title = EXCLUDED.title, description = EXCLUDED.description,\n  order_index = EXCLUDED.order_index, xp_reward = EXCLUDED.xp_reward, access_tier = EXCLUDED.access_tier,\n  estimated_minutes = EXCLUDED.estimated_minutes, is_published = true,\n  mission = EXCLUDED.mission, grammar_note = EXCLUDED.grammar_note, phrases = EXCLUDED.phrases;`
  );
});
line();

// ---------------------------------------------------------------------
// 6. Scoped cleanup of lesson_sections/exercises for exactly these lessons
//    (no natural unique key on either table - same delete-then-insert
//    strategy scripts/migrate-french-a1-units.js already uses via the JS
//    client, expressed here as plain SQL, scoped strictly to French A2/B1
//    slugs so no other course/lesson is ever touched).
// ---------------------------------------------------------------------
line('-- ---------------------------------------------------------------------');
line('-- 6. Scoped cleanup: remove only French A2/B1 lesson_sections/exercises');
line('--    before re-inserting them fresh below (idempotent re-run). Scoped by');
line('--    course_lessons.slug LIKE - never touches other levels/languages.');
line('-- ---------------------------------------------------------------------');
line(
  `DELETE FROM public.lesson_sections\nWHERE lesson_id IN (\n  SELECT id FROM public.course_lessons WHERE slug LIKE 'french-a2-%' OR slug LIKE 'french-b1-%'\n);`
);
line(
  `DELETE FROM public.exercises\nWHERE lesson_id IN (\n  SELECT id FROM public.course_lessons WHERE slug LIKE 'french-a2-%' OR slug LIKE 'french-b1-%'\n);`
);
line();

// ---------------------------------------------------------------------
// 7. lesson_sections
// ---------------------------------------------------------------------
line('-- ---------------------------------------------------------------------');
line('-- 7. lesson_sections (intro / vocabulary_item / dialogue_line / reading)');
line('-- ---------------------------------------------------------------------');
allLessons.forEach((row) => {
  const content = row.content_json || {};
  const lessonRef = `(SELECT id FROM public.course_lessons WHERE slug = ${sqlString(row.slug)})`;
  let orderIndex = 0;

  if (content.intro) {
    line(
      `INSERT INTO public.lesson_sections (lesson_id, type, order_index, line)\nVALUES (${lessonRef}, 'intro', ${orderIndex}, ${sqlString(content.intro)});`
    );
    orderIndex += 1;
  }

  (content.vocabulary || []).forEach((item, index) => {
    line(
      `INSERT INTO public.lesson_sections (lesson_id, type, order_index, word, translation, example)\nVALUES (${lessonRef}, 'vocabulary_item', ${index}, ${sqlString(item.word)}, ${sqlString(item.translation)}, ${sqlString(item.example)});`
    );
  });

  (content.dialogue || []).forEach((item, index) => {
    line(
      `INSERT INTO public.lesson_sections (lesson_id, type, order_index, speaker, line, translation)\nVALUES (${lessonRef}, 'dialogue_line', ${index}, ${sqlString(item.speaker)}, ${sqlString(item.line)}, ${sqlString(item.translation)});`
    );
  });

  const reading = shapeReading(content.reading);
  if (reading) {
    line(
      `INSERT INTO public.lesson_sections (lesson_id, type, order_index, reading_title, reading_text, reading_questions, reading_parts, reading_ordering)\nVALUES (${lessonRef}, 'reading', 0, ${sqlString(reading.title || null)}, ${sqlString(reading.text || '')}, ${sqlJsonb(reading.questions || [])}, ${reading.parts ? sqlJsonb(reading.parts) : 'NULL'}, ${reading.ordering ? sqlJsonb(reading.ordering) : 'NULL'});`
    );
  }
});
line();

// ---------------------------------------------------------------------
// 8. exercises + exercise_options
// ---------------------------------------------------------------------
line('-- ---------------------------------------------------------------------');
line('-- 8. exercises + exercise_options (mcq options via a per-exercise CTE,');
line('--    matching the shape lib/courseLessonsService.js reads)');
line('-- ---------------------------------------------------------------------');
allLessons.forEach((row) => {
  const content = row.content_json || {};
  const lessonRef = `(SELECT id FROM public.course_lessons WHERE slug = ${sqlString(row.slug)})`;

  (content.exercises || []).forEach((exercise, index) => {
    if (exercise.type === 'mcq' && Array.isArray(exercise.options)) {
      const valuesRows = exercise.options
        .map(
          (optionText, optionIndex) =>
            `  (${sqlString(optionText)}, ${sqlBool(optionIndex === Number(exercise.answer))}, ${optionIndex})`
        )
        .join(',\n');
      line(
        `WITH ex AS (\n  INSERT INTO public.exercises (lesson_id, type, prompt, order_index)\n  VALUES (${lessonRef}, ${sqlString(exercise.type)}, ${sqlString(exercise.prompt)}, ${index})\n  RETURNING id\n)\nINSERT INTO public.exercise_options (exercise_id, option_text, is_correct, order_index)\nSELECT ex.id, v.option_text, v.is_correct, v.order_index\nFROM ex, (VALUES\n${valuesRows}\n) AS v(option_text, is_correct, order_index);`
      );
    } else {
      line(
        `INSERT INTO public.exercises (lesson_id, type, prompt, order_index)\nVALUES (${lessonRef}, ${sqlString(exercise.type)}, ${sqlString(exercise.prompt)}, ${index});`
      );
    }
  });
});
line();

// ---------------------------------------------------------------------
// 9. Verification SELECTs
// ---------------------------------------------------------------------
line('-- ---------------------------------------------------------------------');
line('-- 9. Verification (runs inside this same transaction, right before COMMIT,');
line('--    so it reflects every insert/update above): confirms the expected counts.');
line('-- ---------------------------------------------------------------------');
line(`SELECT lv.code AS level, count(DISTINCT cu.id) AS units, count(DISTINCT cl.id) AS activities
FROM public.levels lv
JOIN public.courses c ON c.level_id = lv.id
JOIN public.languages l ON l.id = c.language_id AND l.code = 'french'
LEFT JOIN public.course_units cu ON cu.course_id = c.id
LEFT JOIN public.course_lessons cl ON cl.course_id = c.id
WHERE lv.code IN ('A1', 'A2', 'B1')
GROUP BY lv.code
ORDER BY lv.code;`);
line();
line(`-- Expected: A1 = 12 units / 84 activities (untouched), A2 = 10 / 70, B1 = 10 / 70.`);
line();

line('COMMIT;');
line();

fs.writeFileSync(OUT_PATH, out.join('\n') + '\n', 'utf8');

const insertCount = out.filter((l) => /^INSERT INTO|^WITH ex AS/.test(l)).length;
const deleteCount = out.filter((l) => /^DELETE FROM/.test(l)).length;
console.log(`Wrote ${path.relative(ROOT, OUT_PATH)}`);
console.log(`  lessons: ${allLessons.length} (A2: ${allLessons.filter((r) => r.level === 'A2').length}, B1: ${allLessons.filter((r) => r.level === 'B1').length})`);
console.log(`  INSERT/CTE statements: ${insertCount}, DELETE statements: ${deleteCount}`);
